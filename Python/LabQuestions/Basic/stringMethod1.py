def string_methods_demo():
    text = input("Enter a string: ")

    print("\nString Methods")
    print(f"Original String: {text}")
    print(f"Uppercase: {text.upper()}")  # uppercase
    print(f"Lowercase: {text.lower()}")  # lowercase
    print(f"Title Case: {text.title()}")  # file letteer will be capital of each word
    print(f"Stripped: '{text.strip()}'")  # inverted commas are used to show the spaces
 


string_methods_demo()
